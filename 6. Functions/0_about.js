/*
In JavaScript, functions are like little bundles of code that you can create 
to perform specific tasks. They're incredibly useful because they allow you 
to write a block of code once and then reuse it multiple times without having 
to rewrite it each time.
*/

/*
Imagine you have a recipe for making a sandwich. Instead of writing out 
all the steps every time you want to make a sandwich, you can just write 
them down once and then refer back to that recipe whenever you want to make 
another sandwich. In this analogy:

-> The recipe is like a function.
-> Making a sandwich is like running the function.
*/