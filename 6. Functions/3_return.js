// Return is a js keyword that returns a specific value
// NOTE: ONLY ONE RETURN CAN BE MADE FOR EACH FUNCTION
// NOTE: 'RETURN' CAN RETURN ANY VALUE, OBJECTS, ARRAYS, ETC.
// NOTE: CODE WRITTEN AFTER 'RETURN' WON'T BE EXECUTED

function minus(n1, n2) {
    let res = n1 - n2;
    return res;
}

// If i simply type: minus(12, 10) it would still return 2 but not print it

console.log(minus(12, 10));